export class LogoAnimation extends Component {
	constructor(props) {
		super(props)
		this.state = {}
	}

	runAnimation = (e = false) => {
		if (e) {
			e.preventDefault()
		}
		// const path = anime.path('#LogoBlack path')

		anime({
			targets: [ '#LogoBlack path', '#PlusMinus path' ],
			strokeDashoffset: [ anime.setDashoffset, 0 ],
			easing: 'easeInOutSine',
			duration: 1750,
			delay: function(el, i) {
				return i * 250
			}
			// direction: 'alternate'
			// loop: true
			// loop: true
		})
	}
	componentDidMount() {
		const { background, type } = this.props
		if (type) {
			this.runAnimation()
		}
	}
	render() {
		const { background, type } = this.props
		return (
			<div
				style={{
					height: '100vh',
					overflow: 'scroll',
					padding: '30px',
					width: '100vw',
					position: 'relative',
					display: 'flex',
					background: background,
					justifyContent: 'center',
					alignContent: 'center'
				}}
			>
				{type === 'stroke' ? (
					<StrokeLogo onClick={this.runAnimation} color={background} />
				) : (
					<FillLogo onClick={this.runAnimation} color={background} />
				)}
			</div>
		)
	}
}
